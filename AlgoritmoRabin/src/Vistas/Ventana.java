    public void mostrarDesarrollo()
    {
        if(mensaje.getText().length()<16)
        {
            desarrollo1.append("\t\t **** PROCESO DE CIFRADO ****\n\n");
            desarrollo1.append("CADENA DE BITS INGRESADA: "+mensaje.getText()+"\n");
            desarrollo1.append("CADENA DE BITS DE 16 DIGITOS: "+rabin.getCadenaCompleta()+"\n");
            desarrollo1.append("EQUIVALENCIA EN DECIMAL: "+rabin.getM()+"\n");
            desarrollo1.append("MENSAJE CIFRADO: "+rabin.getC()+"\n");
            desarrollo1.append("EQUIVALENCIA EN BINARIO: "+rabin.getCifrado()+"\n\n");
        }
        else
        {
            desarrollo1.append("\t\t **** PROCESO DE CIFRADO ****\n\n");
            desarrollo1.append("CADENA DE BITS INGRESADA: "+mensaje.getText()+"\n");
            desarrollo1.append("EQUIVALENCIA EN DECIMAL: "+rabin.getM()+"\n");
            desarrollo1.append("MENSAJE CIFRADO: "+rabin.getC()+"\n");
            desarrollo1.append("EQUIVALENCIA EN BINARIO: "+rabin.getCifrado()+"\n\n");
        }
    }
